locations = ["Thiland", "Ooti", "Nanital", "Denmark", "Lakshdveep"]
print(locations)

# Temporarily sorted
print(sorted(locations))

# Original list
print(locations)

print(sorted(locations,reverse=True))

# Original list
print(locations)
# reverse list
locations.reverse()
print(locations)
# reverse again to get original list
locations.reverse()
print(locations)
# sort permanently
locations.sort()
print(locations)
# sort in reverse order
locations.sort(reverse=True)
print(locations)